export const getExerciseById = (id: string): ExerciseDefinition | undefined => {
  return PREDEFINED_EXERCISES.find(ex => ex.id === id);
};

export const getExercisesByMuscle = (muscle: MuscleGroup): ExerciseDefinition[] => {
  return PREDEFINED_EXERCISES.filter(ex => 
    ex.primaryMuscle === muscle || ex.secondaryMuscles.includes(muscle)
  );
};

export const getExercisesByEquipment = (equipment: EquipmentType): ExerciseDefinition[] => {
  return PREDEFINED_EXERCISES.filter(ex => ex.equipment === equipment);
};

export const getExercisesByDifficulty = (difficulty: DifficultyLevel): ExerciseDefinition[] => {
  return PREDEFINED_EXERCISES.filter(ex => ex.difficulty === difficulty);
};
